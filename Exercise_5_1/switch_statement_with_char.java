public class switch_statement_with_char {
	public static void main(String[] args) {
		char c = 'c';

		switch(c) {
			case 'a': 
				System.out.println("Letter A");
				break;
			case 'b': 
				System.out.println("Letter B");
				break;
			case 'c': 
				System.out.println("Letter C");
				break;
			default:
				System.out.println("Default here");
		}
	}
}
